# Variables
# We can create a new Python variable by assigning a value to a label, using
# the = assignment operator.

# Examples
name = "RiteChoice Innovations"
phone = 2348162944038
isMale = True

# Variable name rules
# These are all valid variable names:
# name1
# AGE
# aGE
# a11111
# my_name
# _name

# invalid names are
# 123
# test!
# name%
